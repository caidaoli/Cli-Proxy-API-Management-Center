import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const SOURCE_URL = 'https://models.dev/api.json';
const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = resolve(projectRoot, 'src/data/openaiPricing.generated.ts');

const response = await fetch(SOURCE_URL);
if (!response.ok) {
  throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status} ${response.statusText}`);
}

const payload = await response.json();
const models = payload?.openai?.models;
if (!models || typeof models !== 'object' || Array.isArray(models)) {
  throw new Error('models.dev response is missing openai.models');
}

const readPrice = (value, path) => {
  if (typeof value !== 'number' || !Number.isFinite(value) || value < 0) {
    throw new Error(`Invalid price at ${path}`);
  }
  return value;
};

const entries = Object.entries(models)
  .filter(([, model]) => model?.cost != null)
  .sort(([left], [right]) => left.localeCompare(right))
  .map(([modelId, model]) => {
    const cost = model.cost;
    const pricing = {
      inputPrice: readPrice(cost.input, `openai.models.${modelId}.cost.input`),
      outputPrice: readPrice(cost.output, `openai.models.${modelId}.cost.output`),
    };

    if (cost.cache_read !== undefined) {
      pricing.cacheReadPrice = readPrice(
        cost.cache_read,
        `openai.models.${modelId}.cost.cache_read`
      );
    }
    if (cost.cache_write !== undefined) {
      pricing.cacheWritePrice = readPrice(
        cost.cache_write,
        `openai.models.${modelId}.cost.cache_write`
      );
    }

    const tier = Array.isArray(cost.tiers)
      ? cost.tiers.find((candidate) => candidate?.tier?.type === 'context')
      : undefined;
    if (tier) {
      pricing.tierThreshold = readPrice(
        tier.tier.size,
        `openai.models.${modelId}.cost.tiers[].tier.size`
      );
      pricing.inputPriceHigh = readPrice(tier.input, `openai.models.${modelId}.cost.tiers[].input`);
      pricing.outputPriceHigh = readPrice(
        tier.output,
        `openai.models.${modelId}.cost.tiers[].output`
      );
      if (tier.cache_read !== undefined) {
        pricing.cacheReadPriceHigh = readPrice(
          tier.cache_read,
          `openai.models.${modelId}.cost.tiers[].cache_read`
        );
      }
      if (tier.cache_write !== undefined) {
        pricing.cacheWritePriceHigh = readPrice(
          tier.cache_write,
          `openai.models.${modelId}.cost.tiers[].cache_write`
        );
      }
    }

    return [modelId, pricing];
  });

if (entries.length === 0) {
  throw new Error('models.dev returned no priced OpenAI models');
}

const lines = [
  '// Generated by scripts/update-openai-pricing.mjs from https://models.dev/api.json.',
  '// Do not edit manually.',
  '',
  'export interface OpenAIModelPricing {',
  '  inputPrice: number;',
  '  outputPrice: number;',
  '  cacheReadPrice?: number;',
  '  cacheWritePrice?: number;',
  '  tierThreshold?: number;',
  '  inputPriceHigh?: number;',
  '  outputPriceHigh?: number;',
  '  cacheReadPriceHigh?: number;',
  '  cacheWritePriceHigh?: number;',
  '}',
  '',
  'export const openAIModelPricing = {',
];

for (const [modelId, pricing] of entries) {
  lines.push(`  ${JSON.stringify(modelId)}: ${JSON.stringify(pricing)},`);
}

lines.push('} satisfies Record<string, OpenAIModelPricing>;', '');

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, lines.join('\n'), 'utf8');
process.stdout.write(`Generated ${entries.length} OpenAI prices at ${outputPath}\n`);
